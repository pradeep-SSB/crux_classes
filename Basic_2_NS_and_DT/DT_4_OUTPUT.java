package Basic_2_NS_and_DT;

public class DT_4_OUTPUT {
	public static void main(String[] args) {
		System.out.println(10 +20 + "Hello" + 10 +20); // Key whenever you add anything after string expression JVM takes it as string
		
		System.out.println(2+ ' ' + 5);
		System.out.println("Hello" +'\t' + "World");
		System.out.println("Hello" + "\t" + "World"); // '\t' or "\t" are same
	}

}
